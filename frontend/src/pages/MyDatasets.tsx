interface Photo {
    id: number;
    title: string;
    description: string;
    ipfsHash: string;
}

interface Dataset {
    id: number;
    photos: Photo[];
}

const MyDatasetsPage: React.FC = () => {
    const [datasets, setDatasets] = useState<Dataset[]>([]);
    const [isLoading, setIsLoading] = useState(true);
    const [currentAccount, setCurrentAccount] = useState<string>('');

    const fetchMyDatasets = useCallback(async (account: string) => {
        setIsLoading(true);
        try {
            const provider = await walletConnection.getProvider();
            const registryContract = new ethers.Contract("0x101332f0e23EfB94410441CB678A50800d61BE87", datasetRegistryAbi, provider);
            const socialContract = new ethers.Contract("0x105478CB653F4f26142c9b8daF55f56c97357A89", socialMediaAbi, provider);

            const balance = await registryContract.balanceOf(account);
            if (balance === 0n) {
                setDatasets([]);
                return;
            }

            const ownedDatasets: Dataset[] = [];
            for (let i = 0; i < balance; i++) {
                const datasetId = Number(await registryContract.tokenOfOwnerByIndex(account, i));
                const photoIds = await registryContract.getDatasetPhotos(datasetId);

                const photos: Photo[] = [];
                for (const photoId of photoIds) {
                    const post = await socialContract.getPost(photoId);
                    photos.push({
                        id: Number(photoId),
                        title: post.title,
                        description: post.description,
                        ipfsHash: post.ipfsHash,
                    });
                }
                ownedDatasets.push({ id: datasetId, photos });
            }
            setDatasets(ownedDatasets);
        } catch (error: any) {
            toast.error("Failed to fetch your datasets: " + error.message);
        } finally {
            setIsLoading(false);
        }
    }, []);

    useEffect(() => {
        const init = async () => {
            const account = await walletConnection.getCurrentAccount();
            if (account) {
                setCurrentAccount(account);
                await fetchMyDatasets(account);
            } else {
                setIsLoading(false);
            }
        };
        init();
    }, [fetchMyDatasets]);

    const getIpfsUrl = (hash: string) => `https://gateway.ipfs.io/ipfs/${hash}`;

    if (isLoading) {
        return <div className="container mx-auto px-4 py-8 text-center">Loading your datasets...</div>;
    }

    return (
        <div className="container mx-auto px-4 py-8 max-w-6xl space-y-8">
            <div>
                <h1 className="text-3xl font-bold mb-2">My Purchased Datasets</h1>
                <p className="text-gray-600">Here you can view and download the photos from the datasets you own.</p>
            </div>

            {datasets.length === 0 ? (
                <p>You do not own any datasets yet. Purchase one from the DataDAO Marketplace to see it here.</p>
            ) : (
                datasets.map(dataset => (
                    <Card key={dataset.id}>
                        <CardHeader>
                            <CardTitle>Dataset Access NFT #{dataset.id}</CardTitle>
                            <CardDescription>{dataset.photos.length} photos in this dataset.</CardDescription>
                        </CardHeader>
                        <CardContent className="grid grid-cols-2 md:grid-cols-3 lg:grid-cols-4 gap-4">
                            {dataset.photos.map(photo => (
                                <div key={photo.id} className="border rounded-lg overflow-hidden">
                                    <img src={getIpfsUrl(photo.ipfsHash)} alt={photo.title} className="w-full h-48 object-cover" />
                                    <div className="p-3">
                                        <h3 className="font-semibold truncate">{photo.title}</h3>
                                        <p className="text-xs text-gray-500 truncate">Token ID: {photo.id}</p>
                                        <a href={getIpfsUrl(photo.ipfsHash)} download target="_blank" rel="noopener noreferrer">
                                            <Button variant="outline" size="sm" className="w-full mt-2">Download</Button>
                                        </a>
                                    </div>
                                </div>
                            ))}
                        </CardContent>
                    </Card>
                ))
            )}
        </div>
    );
};

export default MyDatasetsPage;