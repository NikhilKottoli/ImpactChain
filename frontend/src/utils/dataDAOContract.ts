const getContracts = async () => {
    const provider = await walletConnection.getProvider();
    const signer = await provider.getSigner();

    // Using the addresses defined in this function for consistency.
    const DATA_SET_REGISTRY_CONTRACT = "0x101332f0e23EfB94410441CB678A50800d61BE87";
    const DATA_COIN_CONTRACT = "0xf7773581642bb74795B30F34C862309044276161";

    const datasetRegistryContract = new ethers.Contract(DATA_SET_REGISTRY_CONTRACT, datasetRegistryAbi, signer);
    const dataCoinContract = new ethers.Contract(DATA_COIN_CONTRACT, dataCoinAbi, signer);

    return { datasetRegistryContract, dataCoinContract, signer };
};

export const createDataset = async (photoIDs: number[], signature: string) => {
    const { datasetRegistryContract, dataCoinContract, signer } = await getContracts();
    
    // --- REVISED LOGIC ---

    // 1. Get the price per photo from the registry contract.
    const pricePerPhoto = await datasetRegistryContract.pricePerPhoto();
    
    // 2. Calculate the total cost.
    const totalCost = pricePerPhoto * BigInt(photoIDs.length);
    
    toast.info(`Total cost: ${ethers.formatUnits(totalCost, 18)} SIT. Please approve token spending.`);

    // 3. Check current allowance and approve if necessary.
    const ownerAddress = await signer.getAddress();
    const currentAllowance = await dataCoinContract.allowance(ownerAddress, await datasetRegistryContract.getAddress());

    if (currentAllowance < totalCost) {
        // Send the 'approve' transaction to the DataCoin contract.
        const approveTx = await dataCoinContract.approve(await datasetRegistryContract.getAddress(), totalCost);
        toast.info("Waiting for approval transaction to confirm...");
        await approveTx.wait(); 
        toast.success("Approval successful! Now confirming the purchase...");
    } else {
        toast.info("Sufficient allowance already approved. Proceeding with purchase...");
    }

    // 4. Send the 'createDataset' transaction.
    const createTx = await datasetRegistryContract.createDataset(photoIDs, signature);
    await createTx.wait();
    return createTx;
};

