/**
 * POST /api/datadao/update-settings
 * Updates core settings on the DatasetRegistry contract. (Owner-only)
 */
router.post("/update-settings", async (req, res) => {
    const { pricePerPhoto, daoFeePercentage, validatorAddress } = req.body;
    try {
        const registryContract = getContract(process.env.DATASET_REGISTRY_ADDRESS, DATASET_REGISTRY_ABI, ownerSigner);
        let tx;

        if (pricePerPhoto) {
            console.log(`Updating pricePerPhoto to ${pricePerPhoto}...`);
            tx = await registryContract.setPricePerPhoto(ethers.parseEther(pricePerPhoto));
            await tx.wait();
        }
        if (daoFeePercentage) {
            console.log(`Updating daoFeePercentage to ${daoFeePercentage}...`);
            tx = await registryContract.setDaoFeePercentage(daoFeePercentage);
            await tx.wait();
        }
        if (validatorAddress) {
            console.log(`Updating validatorAddress to ${validatorAddress}...`);
            tx = await registryContract.setValidatorAddress(validatorAddress);
            await tx.wait();
        }

        res.status(200).json({ message: "Settings updated successfully." });
    } catch (error) {
        console.error("Failed to update settings:", error.message);
        res.status(500).json({ error: `Failed to update settings: ${error.message}` });
    }
});

