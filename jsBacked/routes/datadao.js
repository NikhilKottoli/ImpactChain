/**
 * GET /api/datadao/tokens-by-label/:label
 * Finds all token_ids in the pipeline for a given label.
 */
router.get("/tokens-by-label/:label", async (req, res) => {
  const { label } = req.params;
  if (!label) {
    return res.status(400).json({ error: "Label parameter is required." });
  }
  try {
    const tokenIds = await supabaseService.getTokenIdsByLabelFromPipeline(label);
    if (!tokenIds || tokenIds.length === 0) {
      return res.status(404).json({ message: "No matching data found for this label." });
    }
    res.json(tokenIds);
  } catch (error) {
    res.status(500).json({ error: `Error fetching tokens by label: ${error.message}` });
  }
});

/**
 * GET /api/datadao/dataset/:datasetId
 * Provides enriched data for a purchased Dataset Access NFT.
 */
router.get("/dataset/:datasetId", async (req, res) => {
    const { datasetId } = req.params;
    try {
        const registryContract = getContract(process.env.DATASET_REGISTRY_ADDRESS, DATASET_REGISTRY_ABI);
        
        // 1. Get the list of photo token IDs from the smart contract
        const photoTokenIds = await registryContract.getDatasetPhotos(datasetId);
        if (photoTokenIds.length === 0) {
            return res.status(404).json({ message: "Dataset not found or is empty." });
        }

        // 2. Enrich the data by fetching post details from Supabase
        const enrichedPhotos = [];
        for (const tokenId of photoTokenIds) {
            const post = await supabaseService.getPostByTokenId(Number(tokenId));
            if (post) {
                enrichedPhotos.push({
                    tokenId: post.token_id,
                    title: post.title,
                    ipfsHash: post.ipfs_hash,
                    creator: post.creator_id,
                });
            }
        }
        res.status(200).json({ datasetId, photos: enrichedPhotos });
    } catch (error) {
        console.error(`Error fetching dataset ${datasetId}:`, error.message);
        res.status(500).json({ error: "Failed to retrieve dataset details." });
    }
});


