/**
 * POST /api/datadao/mint
 * Mints new DataCoin tokens. (Owner-only)
 * This logic is based on your verified minting script.
 */
router.post("/mint", async (req, res) => {
  const { recipient, amount } = req.body;
  if (!recipient || !amount) {
    return res.status(400).json({ error: "Recipient and amount are required." });
  }

  try {
    console.log(`Preparing to mint ${amount} STC to ${recipient}...`);
    
    // Get contract instance using the existing helper and owner signer
    const dataCoinContract = getContract(process.env.DATA_COIN_ADDRESS, DATA_COIN_ABI, ownerSigner);

    // Convert the amount to the correct unit (18 decimals for standard ERC20)
    // Using parseUnits as per your script for clarity.
    const amountInWei = ethers.parseUnits(amount, 18);

    console.log('Sending mint transaction...');
    const tx = await dataCoinContract.mint(recipient, amountInWei);
    
    console.log(`Transaction sent! Hash: ${tx.hash}`);
    console.log('Waiting for transaction to be confirmed...');
    
    await tx.wait();

    console.log(`\n✅ Minting Successful! ${amount} STC have been sent to ${recipient}.`);
    res.status(200).json({ 
        message: "Mint successful", 
        recipient: recipient,
        amount: amount,
        transactionHash: tx.hash 
    });

  } catch (error) {
    console.error('\n❌ Minting Failed!');
    console.error(error);
    res.status(500).json({ error: `Minting failed: ${error.message}` });
  }
});

