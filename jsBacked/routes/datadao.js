/**
 * POST /api/datadao/validate
 * Creates a cryptographic signature for a list of token IDs.
 */
router.post("/validate", async (req, res) => {
  const { photoIDs } = req.body;

  if (!photoIDs || !Array.isArray(photoIDs) || photoIDs.length === 0) {
    return res.status(400).json({ error: "A non-empty 'photoIDs' array is required." });
  }

  try {
    const validatorWallet = new ethers.Wallet(process.env.VALIDATOR_PRIVATE_KEY);
    const messageHash = ethers.solidityPackedKeccak256(["uint256[]"], [photoIDs]);
    const signature = await validatorWallet.signMessage(ethers.getBytes(messageHash));

    res.status(200).json({ photoIDs, apiSignature: signature });
  } catch (error) {
    res.status(500).json({ error: `Failed to create dataset signature: ${error.message}` });
  }
});


