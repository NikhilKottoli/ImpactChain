// --- Ethers Provider & Signer Setup ---
const provider = new ethers.JsonRpcProvider(process.env.RPC_URL);
// This signer uses the contract owner's private key for admin tasks like minting
const ownerSigner = new ethers.Wallet(process.env.PRIVATE_KEY, provider);

// --- Helper Function to get Contract Instances ---
const getContract = (address, abi, signer = null) => {
  if (!address) throw new Error("Contract address is missing.");
  return new ethers.Contract(address, abi, signer || provider);
};


