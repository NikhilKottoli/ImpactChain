/**
 * Mints a new PhotoNFT on the blockchain.
 * @param {string} contributorAddress - The address of the photo owner.
 * @param {string} cid - The IPFS CID from Lighthouse.
 * @param {string} labels - Comma-separated labels.
 * @returns {Promise<number>} The ID of the newly minted token.
 */
async function mintPhotoNFT(contributorAddress, cid, labels) {
    console.log(`Minting NFT for ${contributorAddress} with CID ${cid}`);
    const tx = await socialMediaPostsContract.mintPhotoNFT(contributorAddress, cid, labels);
    const receipt = await tx.wait();
    
    const transferEvent = receipt.logs.find(log => {
        try {
            const parsedLog = socialMediaPostsContract.interface.parseLog(log);
            return parsedLog && parsedLog.name === 'Transfer';
        } catch (error) {
            return false;
        }
    });

    if (!transferEvent) {
        throw new Error("Could not find Transfer event in transaction receipt to get tokenId.");
    }
    const tokenId = transferEvent.args.tokenId;
    console.log(`Minted NFT with tokenId: ${tokenId}`);
    return Number(tokenId);
}

/**
 * Signs a message containing the dataset's photo token IDs.
 */
async function signDataset(tokenIds) {
    // 1. Create the core data hash, exactly like in Solidity.
    const messageHash = ethers.keccak256(
        ethers.solidityPacked(["uint256[]"], [tokenIds])
    );
    console.log(`[Ethers Service] DataHash to be signed: ${messageHash}`);

    // 2. Sign the 32-byte hash. 
    // wallet.signMessage() will automatically prepend "\x19Ethereum Signed Message:\n32"
    // and hash it again, which is exactly what `toEthSignedMessageHash` does in Solidity.
    const signature = await validatorWallet.signMessage(ethers.getBytes(messageHash));
    
    console.log(`[Ethers Service] Generated Signature: ${signature}`);
    return signature;
}

